public class App {
    public static void main(String[] args) throws Exception {
        
        //OPERADORES

        //OPERADORES ARITIMÉTICOS
            // São utilizados para realizar operações matemáticas = +, -, *, /

            System.out.println(1+1);

        //OPERADORES UNÁRIOS
            //São operadores que sua utilização é feita com outro operador aritimético ex: incrementar ou decrementar valores ++ | -- | ! negação

            System.out.println(!true);

            int numero = 0;
            System.out.println(++numero);
            System.out.println(--numero);
            
        //OPERADORES RELACIONAIS
            //São utilizados para 

        //OPERADORES LÓGICOS
            // && , ||

        //OPERADORES TERNÁRIOS
            //Expressão condicional ? condição verdadeira : condição falsa

            int num = 10;
            boolean result = num == 5 ? true : false ;
            System.out.println(result);
    }
}
